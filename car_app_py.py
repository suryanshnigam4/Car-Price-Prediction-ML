import streamlit as st
import pandas as pd
import joblib

# -------------------------------
# Page Configuration
# -------------------------------
st.set_page_config(
    page_title="Car Price Prediction",
    page_icon="🚗",
    layout="centered"
)

# -------------------------------
# Load Trained Model
# -------------------------------
try:
    model = joblib.load("car_price_model.pkl")
except FileNotFoundError:
    st.error("❌ Model file 'car_price_model.pkl' not found.")
    st.stop()

# -------------------------------
# App Title
# -------------------------------
st.title("🚗 Car Price Prediction")
st.markdown("Predict the selling price of a used car using Machine Learning.")

st.divider()

# -------------------------------
# User Inputs
# -------------------------------
present_price = st.number_input(
    "Present Price (in Lakhs)",
    min_value=0.0,
    value=5.0,
    step=0.1
)

kms_driven = st.number_input(
    "Kilometers Driven",
    min_value=0,
    value=30000,
    step=1000
)

fuel_type = st.selectbox(
    "Fuel Type",
    ["Petrol", "Diesel", "CNG"]
)

seller_type = st.selectbox(
    "Seller Type",
    ["Dealer", "Individual"]
)

transmission = st.selectbox(
    "Transmission",
    ["Manual", "Automatic"]
)

owner = st.selectbox(
    "Previous Owners",
    [0, 1, 2, 3]
)

car_age = st.slider(
    "Car Age (Years)",
    min_value=0,
    max_value=25,
    value=5
)

# -------------------------------
# Encode Inputs
# -------------------------------
fuel_dict = {
    "CNG": 0,
    "Diesel": 1,
    "Petrol": 2
}

seller_dict = {
    "Dealer": 0,
    "Individual": 1
}

transmission_dict = {
    "Automatic": 0,
    "Manual": 1
}

input_data = pd.DataFrame(
    [[
        present_price,
        kms_driven,
        fuel_dict[fuel_type],
        seller_dict[seller_type],
        transmission_dict[transmission],
        owner,
        car_age
    ]],
    columns=[
        "Present_Price",
        "Kms_Driven",
        "Fuel_Type",
        "Seller_Type",
        "Transmission",
        "Owner",
        "Car_Age"
    ]
)

# -------------------------------
# Prediction
# -------------------------------
if st.button("Predict Price", use_container_width=True):
    prediction = model.predict(input_data)

    st.success(
        f"💰 Estimated Selling Price: ₹ {prediction[0]:.2f} Lakhs"
    )

st.divider()
st.caption("Built using Streamlit • Scikit-learn • Python")